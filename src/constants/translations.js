export const useTranslation = (lang = 'en') => {
  const dict = translations[lang] ?? translations.en;
  return (key, params = {}) => {
    let text = dict[key] ?? translations.en[key] ?? key;
    Object.entries(params).forEach(([k, v]) => { text = text.replace(`{${k}}`, v); });
    return text;
  };
};
